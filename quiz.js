function calcResult() {
  const scores = {};
  Object.keys(ARCHETYPES).forEach(k => scores[k] = 0);

  answers.forEach((aIndex, qIndex) => {
    if (aIndex === null) return;
    const arc = QUESTIONS[qIndex].answers[aIndex].arc;
    scores[arc] = (scores[arc] || 0) + 1;
  });

  // Находим максимум
  let maxScore = 0;
  let winner = 'yaga';
  Object.entries(scores).forEach(([arc, score]) => {
    if (score > maxScore) { maxScore = score; winner = arc; }
  });
  return { winner, scores };
}

