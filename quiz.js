function renderQuestion(index, animate = true) {
  const q = QUESTIONS[index];
  const pct = Math.round((index / QUESTIONS.length) * 100);
  progressFill.style.width = pct + '%';
  questionCounter.textContent = `Вопрос ${index + 1} из ${QUESTIONS.length}`;
  progressText.textContent = `${index + 1} / ${QUESTIONS.length}`;
  btnBack.disabled = index === 0;

  const html = `
    <p class="question-text">${q.q}</p>
    <ul class="answers-list" role="list">
      ${q.answers.map((a, i) => `
        <li>
          <button
            class="answer-btn has-dot ${answers[index] === i ? 'selected' : ''}"
            data-index="${i}"
            data-testid="answer-${index}-${i}"
          >${a.text}</button>
        </li>
      `).join('')}
    </ul>
  `;

  if (animate) {
    questionArea.classList.add('q-fade-out');
    setTimeout(() => {
      questionArea.innerHTML = html;
      questionArea.classList.remove('q-fade-out');
      questionArea.classList.add('q-fade-in');
      bindAnswers(index);
      setTimeout(() => questionArea.classList.remove('q-fade-in'), 350);
    }, 200);
  } else {
    questionArea.innerHTML = html;
    bindAnswers(index);
  }
}

function bindAnswers(qIndex) {
  document.querySelectorAll('.answer-btn').forEach(btn => {
    btn.addEventListener('click', () => {
      const aIndex = parseInt(btn.dataset.index);
      answers[qIndex] = aIndex;

      // Визуально отмечаем
      document.querySelectorAll('.answer-btn').forEach(b => b.classList.remove('selected'));
      btn.classList.add('selected');

      // Через 400ms — следующий вопрос или результат
      setTimeout(() => {
        if (qIndex < QUESTIONS.length - 1) {
          current = qIndex + 1;
          renderQuestion(current);
        } else {
          showResult();
        }
      }, 400);
    });
  });
}

